int16_t MotorMaxSpeed = 30;
int16_t MotorCurrentSpeed = 0;
int16_t MotorDirection = 1;
int16_t MotorRampAccFactor = 2;

int16_t MC_TargetSpeed = -15;
int16_t MC_CurrentSpeed = 0;
void SetMotorTick(){
	MC_ProgramSpeedRampMotor1(RPM_2_SPEED_UNIT(MotorMaxSpeed * MotorDirection), 1);
	osDelay(MotorCurrentSpeed / 3);
	MC_ProgramSpeedRampMotor1(RPM_2_SPEED_UNIT(0), 0);
	osDelay((MotorMaxSpeed - MotorCurrentSpeed) / 3);
}

void MotorStart(){
	SetMotorTick();
	while (MC_GetSTMStateMotor1() != 6){
		MC_StartMotor1();
		osDelay(1);
	}
}

void MotorStop(){
	while (MC_GetSTMStateMotor1() != 0){
		MC_StopMotor1();
		osDelay(1);
	}
}
